    private void initWebcam() {
        Dimension size = WebcamResolution.QVGA.getSize();
        webcam = Webcam.getWebcams().get(0);
        webcam.setViewSize(size);

        panel = new WebcamPanel(webcam);
        panel.setPreferredSize(size);
        panel.setFPSDisplayed(true);

        jPanel2.add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 470, 300));

        executor.execute(this);
    }

    @Override
    public void run() {
        do {
            try {
                Thread.sleep(100);
            } catch (InterruptedException ex) {
                Logger.getLogger(ReadQRCode.class.getName()).log(Level.SEVERE, null, ex);
            }
            Result result = null;
            BufferedImage image = null;

            if (webcam.isOpen()) {
                if ((image = webcam.getImage()) == null) {
                    continue;
                }
            }

            LuminanceSource source = new BufferedImageLuminanceSource(image);
            BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

            try {
                result = new MultiFormatReader().decode(bitmap);
            } catch (NotFoundException ex) {
                Logger.getLogger(ReadQRCode.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (result != null) {
                String qrText = result.getText();
                parseQRCode(qrText);
            }

        } while (true);
    }

    public void addObserver(QRCodeObserver observer) {
        observers.add(observer);
    }

    private void parseQRCode(String qrText) {
        // Assuming QR code text is formatted as "ID ||Name|DOB|Gender|Address|Date"
      
                // Notify all observers about the result
                for (QRCodeObserver observer : observers) {
                    observer.onQRCodeRead(qrText);
                    dispose();
                    webcam.close();
               
            
        }
    }

    public interface QRCodeObserver {

        void onQRCodeRead(String qrText);
    }

