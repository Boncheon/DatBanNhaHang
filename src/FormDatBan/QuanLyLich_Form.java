    private JPanel createTopPanel() {
        JPanel topPanel = new JPanel();
        JLabel jLabel1 = new JLabel("Ngày đặt bàn");
        jDateChooser1 = new com.toedter.calendar.JDateChooser();

        jDateChooser1.addPropertyChangeListener(new PropertyChangeListener() {
            @Override
            public void propertyChange(PropertyChangeEvent evt) {
                if ("date".equals(evt.getPropertyName())) {
                    Date selectedDate = jDateChooser1.getDate();
                    granttChartPanel.setReservationDate(selectedDate);
                }
            }
        });


        JPanel legendPanel = new JPanel();
        legendPanel.setLayout(new FlowLayout(FlowLayout.LEFT, 10, 0));

        JLabel label1 = new JLabel("Chưa checkin");
        label1.setOpaque(true);
        label1.setBackground(Color.BLUE);
        legendPanel.add(label1);

        JLabel label2 = new JLabel("Đã checkin");
        label2.setOpaque(true);
        label2.setBackground(Color.GREEN);
        legendPanel.add(label2);

        JLabel label3 = new JLabel("Hủy");
        label3.setOpaque(true);
        label3.setBackground(Color.RED);
        legendPanel.add(label3);
        topPanel.add(jLabel1);
        topPanel.add(jDateChooser1);
        topPanel.add(legendPanel, BorderLayout.SOUTH);

        return topPanel;
    }
    

